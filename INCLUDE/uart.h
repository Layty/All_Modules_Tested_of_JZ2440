#ifndef _UART_H
#define _UART_H

/**********************************************************************************
  * @brief       : 	初始化串口0相关资源
  * @param[in]   : 	无
  * @param[out]  : 	无
  * @return      : 	无
  * @others      : 	115200,8n1
***********************************************************************************/
void InitUart0(void);

/**********************************************************************************
  * @brief       : 	串口0输出一个字符
  * @param[in]   : 	c	待输出字符
  * @param[out]  : 	无
  * @return      : 	无
  * @others      : 	115200,8n1
***********************************************************************************/
int PutChar(int c);

/**********************************************************************************
  * @brief       : 	串口0获得一个字符
  * @param[in]   : 	无
  * @param[out]  : 	无
  * @return      : 	返回获得的数据
  * @others      : 	115200,8n1
***********************************************************************************/
int GetChar(void);

/**********************************************************************************
  * @brief       : 	串口0输出一个字符串
  * @param[in]   : 	s	待输出字符串指针
  * @param[out]  : 	无
  * @return      : 	无
  * @others      : 	115200,8n1
***********************************************************************************/
int Puts(const char *s);

#endif

